package nz.ac.auckland.se281;

public class Profiles {
  private String username;
  private String age;

  public Profiles(String un, String a) {
    username = un;
    age = a;
  }

  public String getUsername() {
    String name = username;
    return name;
  }
  public String getAge() {
    String old = age;
    return old;
  }
}
